        public static byte[] NewBinary(List<PlayRecord> listPlayRecord)
        {
            byte[] binary = new byte[listPlayRecord.Count];

            for (int i = 0; i < listPlayRecord.Count; i++)
            {
                if (listPlayRecord[i].Unlocked == false)
                {
                    binary[i] = 0x00;
                }
                else
                {
                    binary[i] = 0x01;
                }
            }

            return binary;
        }
